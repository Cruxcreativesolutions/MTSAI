export function ContactForm() {
  const [formData, setFormData] = useState({
    firstName: "",
    lastName: "",
    company: "",
    jobTitle: "",
    email: "",
    phone: "",
    message: "",
  });

  const [errors, setErrors] = useState({});

  const handleInputChange = (e) => {
    const { name, value } = e.target;
    setFormData((prev) => ({
      ...prev,
      [name]: value,
    }));

    if (errors[name]) {
      setErrors((prev) => ({
        ...prev,
        [name]: "",
      }));
    }
  };

  const validateForm = () => {
    const newErrors = {};

    if (!formData.firstName.trim()) {
      newErrors.firstName = "First name is required";
    }

    if (!formData.lastName.trim()) {
      newErrors.lastName = "Last name is required";
    }

    if (!formData.email.trim()) {
      newErrors.email = "Email address is required";
    } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(formData.email)) {
      newErrors.email = "Please enter a valid email address";
    }

    if (!formData.message.trim()) {
      newErrors.message = "Message is required";
    }

    return newErrors;
  };

  const handleSubmit = (e) => {
    e.preventDefault();

    const validationErrors = validateForm();

    if (Object.keys(validationErrors).length > 0) {
      setErrors(validationErrors);
      toast.error("Please fill in all required fields");
      return;
    }

    setErrors({});
    toast.success("Thank you! Your message has been sent successfully.");

    // Handle form submission here
    console.log("Form submitted:", formData);

    setFormData({
      firstName: "",
      lastName: "",
      company: "",
      jobTitle: "",
      email: "",
      phone: "",
      message: "",
    });
  };

  return (
    <div className="max-w-7xl mx-auto px-4 py-16">
      {/* Header */}
      <div className="text-center mb-12">
        <h1 className="text-4xl md:text-5xl font-bold text-balance mb-6">
          Get in <span className="text-[#2563eb]">Touch</span>
        </h1>
        <p className="text-lg text-muted-foreground max-w-3xl mx-auto text-balance">
          Have a question, a partnership idea, or want to see MTSAi in action?
          Fill out the form below and our team will get back to you shortly.
        </p>
      </div>

      {/* Form */}
      <form onSubmit={handleSubmit} className="space-y-6">
        {/* First row - Name fields */}
        <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
          <div>
            <Input
              type="text"
              name="firstName"
              placeholder="First Name *"
              value={formData.firstName}
              onChange={handleInputChange}
              className={`h-14 rounded-full border-[#bfbfbf] px-6 text-base placeholder:text-[#9ca3af] ${
                errors.firstName
                  ? "border-red-500 focus-visible:ring-red-500"
                  : ""
              }`}
              required
            />
            {errors.firstName && (
              <p className="text-red-500 text-sm mt-1 ml-6">
                {errors.firstName}
              </p>
            )}
          </div>
          <div>
            <Input
              type="text"
              name="lastName"
              placeholder="Last Name *"
              value={formData.lastName}
              onChange={handleInputChange}
              className={`h-14 rounded-full border-[#bfbfbf] px-6 text-base placeholder:text-[#9ca3af] ${
                errors.lastName
                  ? "border-red-500 focus-visible:ring-red-500"
                  : ""
              }`}
              required
            />
            {errors.lastName && (
              <p className="text-red-500 text-sm mt-1 ml-6">
                {errors.lastName}
              </p>
            )}
          </div>
        </div>

        {/* Second row - Company and Job Title */}
        <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
          <Input
            type="text"
            name="company"
            placeholder="Company"
            value={formData.company}
            onChange={handleInputChange}
            className="h-14 rounded-full border-[#bfbfbf] px-6 text-base placeholder:text-[#9ca3af]"
          />
          <Input
            type="text"
            name="jobTitle"
            placeholder="Job Title"
            value={formData.jobTitle}
            onChange={handleInputChange}
            className="h-14 rounded-full border-[#bfbfbf] px-6 text-base placeholder:text-[#9ca3af]"
          />
        </div>

        {/* Third row - Email and Phone */}
        <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
          <div>
            <Input
              type="email"
              name="email"
              placeholder="Email Address *"
              value={formData.email}
              onChange={handleInputChange}
              className={`h-14 rounded-full border-[#bfbfbf] px-6 text-base placeholder:text-[#9ca3af] ${
                errors.email ? "border-red-500 focus-visible:ring-red-500" : ""
              }`}
              required
            />
            {errors.email && (
              <p className="text-red-500 text-sm mt-1 ml-6">{errors.email}</p>
            )}
          </div>
          <Input
            type="tel"
            name="phone"
            placeholder="Phone Number"
            value={formData.phone}
            onChange={handleInputChange}
            className="h-14 rounded-full border-[#bfbfbf] px-6 text-base placeholder:text-[#9ca3af]"
          />
        </div>

        {/* Message field - full width */}
        <div>
          <Textarea
            name="message"
            placeholder="Message *"
            value={formData.message}
            onChange={handleInputChange}
            className={`min-h-32 rounded-3xl border-[#bfbfbf] px-6 py-4 text-base placeholder:text-[#9ca3af] resize-none ${
              errors.message ? "border-red-500 focus-visible:ring-red-500" : ""
            }`}
            rows={6}
          />
          {errors.message && (
            <p className="text-red-500 text-sm mt-1 ml-6">{errors.message}</p>
          )}
        </div>

        {/* Submit button */}
        <div className="flex justify-center pt-4">
          <Button
            type="submit"
            className="h-14 px-12 rounded-full bg-transparent border-2 border-[#53aa00] text-[#53aa00] hover:bg-[#53aa00] hover:text-white transition-colors text-lg font-medium"
            variant="outline"
          >
            Submit
          </Button>
        </div>
      </form>

      {/* Footer text */}
      <div className="text-center mt-8 space-y-2">
        <p className="text-sm text-muted-foreground">
          By submitting the form, you agree to the{" "}
          <a href="#" className="text-[#53aa00] hover:underline">
            Terms of Use
          </a>
          .
        </p>
        <p className="text-sm text-muted-foreground">
          Any data you provide will be processed according to our{" "}
          <a href="#" className="text-[#53aa00] hover:underline">
            Privacy Policy
          </a>
          .
        </p>
      </div>
    </div>
  );
}
